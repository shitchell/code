

Object.prototype.isLove = function () {
  return this.isPatient()
      && this.isKind()
      && ! this.doesEnvy()
      && ! this.doesBoast()
      && ! this.isProud()
      && ! this.dishonorsOthers()
      && ! this.isSelfSeeking()
      && ! this.isEasilyAngered()
      && ! this.recordsWrongs()
      && ! this.delightsInEvil()
      && this.rejoicesInTruth()
      && this.protects()
      && this.trusts()
      && this.hopes()
      && this.perseveres()
      && ! this.fails()
}



